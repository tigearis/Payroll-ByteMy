const pieData = [
  { name: "Category A", value: 540 },
  { name: "Category B", value: 300 },
  { name: "Category C", value: 150 },
  { name: "Category D", value: 100 },
];

const COLORS = ["#0088FE", "#00C49F", "#FFBB28", "#FF8042"];

interface ChartProps {
  type?: "area" | "bar" | "line" | "pie";
  data?: any[];
  title?: string;
  xDataKey?: string;
  yDataKey?: string;
  height?: number;
}

/**
 * A reusable chart component that supports multiple chart types
 */
export default function Chart({
  type = "area",
  data = sampleData,
  title = "Chart",
  xDataKey = "month",
  yDataKey = "value",
  height = 400,
}: ChartProps) {
  const renderChart = (): ReactElement => {
    switch (type) {
      case "area":
        return (
          <AreaChart
            data={data}
            margin={{ top: 10, right: 30, left: 0, bottom: 0 }}
          >
            <defs>
              <linearGradient id="colorValue" x1="0" y1="0" x2="0" y2="1">
                <stop offset="5%" stopColor="#8884d8" stopOpacity={0.8} />
                <stop offset="95%" stopColor="#8884d8" stopOpacity={0} />
              </linearGradient>
            </defs>
            <CartesianGrid strokeDasharray="3 3" />
            <XAxis dataKey={xDataKey} />
            <YAxis />
            <Tooltip />
            <Area
              type="monotone"
              dataKey={yDataKey}
              stroke="#8884d8"
              fillOpacity={1}
              fill="url(#colorValue)"
            />
          </AreaChart>
        );

      case "bar":
        return (
          <BarChart
            data={data}
            margin={{ top: 10, right: 30, left: 0, bottom: 0 }}
          >
            <CartesianGrid strokeDasharray="3 3" />
            <XAxis dataKey={xDataKey} />
            <YAxis />
            <Tooltip />
            <Bar dataKey={yDataKey} fill="#8884d8" />
          </BarChart>
        );

      case "line":
        return (
          <LineChart
            data={data}
            margin={{ top: 10, right: 30, left: 0, bottom: 0 }}
          >
            <CartesianGrid strokeDasharray="3 3" />
            <XAxis dataKey={xDataKey} />
            <YAxis />
            <Tooltip />
            <Line
              type="monotone"
              dataKey={yDataKey}
              stroke="#8884d8"
              strokeWidth={2}
            />
          </LineChart>
        );

      case "pie":
        return (
          <PieChart>
            <Pie
              data={type === "pie" ? pieData : data}
              cx="50%"
              cy="50%"
              labelLine={false}
              label={({ name, percent }) =>
                `${name}: ${((percent || 0) * 100).toFixed(0)}%`
              }
              outerRadius={height / 3}
              fill="#8884d8"
              dataKey="value"
            >
              {(type === "pie" ? pieData : data).map((entry, index) => (
                <Cell
                  key={`cell-${index}`}
                  fill={COLORS[index % COLORS.length]}
                />
              ))}
            </Pie>
            <Tooltip />
          </PieChart>
        );

      default:
        // Provide a fallback element instead of null
        return <AreaChart data={[]} />;
    }
  };

  return (
    <Card>
      <CardHeader>
        <CardTitle>{title}</CardTitle>
      </CardHeader>
      <CardContent>
        <div style={{ width: "100%", height }}>
          <ResponsiveContainer width="100%" height="100%">
            {renderChart()}
          </ResponsiveContainer>
        </div>
      </CardContent>
    </Card>
  );
}

export {
  ChartContainer,
  ChartTooltip,
  ChartTooltipContent,
  ChartLegend,
  ChartLegendContent,
  ChartStyle,
};
